"""
Multi-cluster, two-stage autoencoder search for cluster-wise gene ranking.

Description
-----------
This script performs autoencoder-based gene ranking for multiple selected clusters
generated by the SignalPath-Finder pipeline. For each cluster, it extracts the
corresponding genes from the expression matrix, standardizes the data, and applies
a two-stage hyperparameter search across multiple random seeds to identify robust
autoencoder configurations. Gene importance is then derived from encoder weights,
and recovery of known downstream genes is evaluated within the top-ranked genes.

Input
-----
1. Cluster assignment file (.csv)
   - Contains gene IDs and their assigned cluster labels.
   - Example: gene_cluster_assignments_CHOSEN_K_seed_x.csv

2. Gene expression matrix (.txt or .tsv)
   - Rows represent genes and columns represent samples.
   - The first column must contain gene IDs.
   - Example: SDX_95_samples.txt

3. Known downstream gene list
   - Provided within the script as KNOWN_DOWNSTREAM_GENES.

Output
------
For each selected cluster, the script generates:
1. Stage A search results across sampled hyperparameter settings
2. Combined Stage A and Stage B master result table
3. Per-seed full gene ranking files
4. Top-5 selected seed summaries
5. Copied ranking files for the top-performing seeds

Additionally, the script produces:
6. A multi-cluster summary file combining the best results across all clusters

Workflow
--------
1. Load cluster assignments and gene expression matrix.
2. Select genes belonging to clusters of interest.
3. Standardize cluster-specific gene expression profiles.
4. Run Stage A random hyperparameter search across multiple seeds.
5. Select top-performing configurations from Stage A.
6. Re-run selected configurations in Stage B using a larger seed range.
7. Compute gene importance scores from encoder-derived weights.
8. Evaluate recovery of known downstream genes among top-ranked genes.
9. Export per-cluster and overall summary results.
"""

