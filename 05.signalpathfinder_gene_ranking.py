def set_seed(seed: int) -> None:
    np.random.seed(seed)
    tf.random.set_seed(seed)
    random.seed(seed)


def sample_config(grid: dict) -> dict:
    return {key: random.choice(values) for key, values in grid.items()}


def config_id(params: dict) -> str:
    keys = [
        "encoding_dim",
        "depth",
        "activation",
        "l1_reg",
        "l2_reg",
        "dropout_rate",
        "noise_sigma",
        "lr",
        "batch_size",
        "top_k",
        "score_type",
    ]
    config_string = ";".join(f"{key}={params[key]}" for key in keys)
    return hashlib.md5(config_string.encode("utf-8")).hexdigest()[:10]


def build_autoencoder(input_dim: int, params: dict) -> Model:
    regularizer = None
    if params["l1_reg"] > 0 and params["l2_reg"] > 0:
        regularizer = l1_l2(params["l1_reg"], params["l2_reg"])
    elif params["l1_reg"] > 0:
        regularizer = l1(params["l1_reg"])
    elif params["l2_reg"] > 0:
        regularizer = l2(params["l2_reg"])

    model_input = Input(shape=(input_dim,), name="input")
    x = model_input

    if params["noise_sigma"] > 0:
        x = GaussianNoise(params["noise_sigma"], name="gaussian_noise")(x)
    if params["dropout_rate"] > 0:
        x = Dropout(params["dropout_rate"], name="input_dropout")(x)

    activation = params["activation"]
    encoding_dim = params["encoding_dim"]

    if params["depth"] == "shallow":
        encoded = Dense(
            encoding_dim,
            activation=activation,
            kernel_regularizer=regularizer,
            name="bottleneck",
        )(x)
        decoded = Dense(
            input_dim,
            activation="linear",
            kernel_regularizer=regularizer,
            name="decoder_out",
        )(encoded)
    else:
        hidden_encoder = Dense(
            encoding_dim * 2,
            activation=activation,
            kernel_regularizer=regularizer,
            name="enc_h1",
        )(x)
        encoded = Dense(
            encoding_dim,
            activation=activation,
            kernel_regularizer=regularizer,
            name="bottleneck",
        )(hidden_encoder)
        hidden_decoder = Dense(
            encoding_dim * 2,
            activation=activation,
            kernel_regularizer=regularizer,
            name="dec_h1",
        )(encoded)
        decoded = Dense(
            input_dim,
            activation="linear",
            kernel_regularizer=regularizer,
            name="decoder_out",
        )(hidden_decoder)

    return Model(model_input, decoded, name="AE")


def train_autoencoder(data: pd.DataFrame, params: dict, seed: int) -> tuple[Model, float]:
    set_seed(seed)

    x = data.values.astype(np.float32)
    x_train, x_val = train_test_split(x, test_size=0.2, random_state=seed, shuffle=True)

    model = build_autoencoder(x.shape[1], params)
    model.compile(optimizer=Adam(learning_rate=params["lr"]), loss="mse")

    callbacks = [
        EarlyStopping(monitor="val_loss", patience=ES_PATIENCE, restore_best_weights=True),
        ReduceLROnPlateau(monitor="val_loss", factor=0.5, patience=LR_PATIENCE, min_lr=1e-6),
    ]

    model.fit(
        x_train,
        x_train,
        validation_data=(x_val, x_val),
        epochs=MAX_EPOCHS,
        batch_size=params["batch_size"],
        verbose=0,
        callbacks=callbacks,
    )

    x_hat = model.predict(x_val, verbose=0)
    val_mse = float(np.mean((x_val - x_hat) ** 2))
    return model, val_mse


def compute_gene_importance(model: Model, genes: list[str], score_type: str) -> pd.Series:
    try:
        w_input_to_h1 = model.get_layer("enc_h1").get_weights()[0]
        w_h1_to_bottleneck = model.get_layer("bottleneck").get_weights()[0]
        kernel = w_input_to_h1 @ w_h1_to_bottleneck
    except ValueError:
        kernel = model.get_layer("bottleneck").get_weights()[0]

    kernel = np.nan_to_num(kernel, nan=0.0, posinf=0.0, neginf=0.0)
    if kernel.shape[0] != len(genes):
        raise ValueError(f"Kernel rows {kernel.shape[0]} do not match number of genes {len(genes)}.")

    weights = pd.DataFrame(kernel, index=genes)

    if score_type == "abs-sum":
        importance = weights.abs().sum(axis=1)
    elif score_type == "abs-max":
        importance = weights.abs().max(axis=1)
    else:
        raise ValueError(f"Unknown score type: {score_type}")

    return importance.sort_values(ascending=False)


def evaluate_recovery(importance: pd.Series, known_downstream_genes: list[str], top_k: int) -> dict:
    """
    Evaluate recovery of known downstream genes by restricting the reference list
    to the ranked gene universe before computing expected counts, fold enrichment,
    and hypergeometric p-values.
    """
    top_k = min(top_k, len(importance))
    top_genes = set(importance.head(top_k).index)
    universe = set(importance.index)
    n_universe = len(universe)

    reference = set(known_downstream_genes) & universe
    k_universe = len(reference)
    k_top = len(top_genes & reference)
    expected = (top_k * k_universe / n_universe) if n_universe > 0 else np.nan
    fold = (k_top / expected) if (expected and expected > 0) else np.nan

    if k_universe == 0 or n_universe == 0:
        p_value = 1.0
    else:
        p_value = 1 - hypergeom.cdf(k_top - 1, n_universe, k_universe, top_k)

    return {
        "kdg_topk": k_top,
        "kdg_fold_enrichment": fold,
        "kdg_expected": expected,
        "kdg_p_value": p_value,
        "kdg_in_universe": k_universe,
        "n_universe": n_universe,
    }


def save_full_ranking(importance: pd.Series, out_path: str, top_k: int) -> None:
    ranks = np.arange(1, len(importance) + 1, dtype=int)
    genes = importance.index.to_numpy()

    is_kdg = np.isin(genes, list(KNOWN_DOWNSTREAM_GENE_SET))
    in_top_k = ranks <= min(top_k, len(importance))

    pd.DataFrame(
        {
            "gene": genes,
            "score": importance.values,
            "rank": ranks,
            "in_topk": in_top_k,
            "is_known_downstream_gene": is_kdg,
            "label": np.where(is_kdg, "KDG", "None"),
        }
    ).to_csv(out_path, index=False)


def run_one_seed(args: tuple) -> dict:
    seed, params, genes, data, per_seed_dir = args
    tf.config.threading.set_intra_op_parallelism_threads(1)
    tf.config.threading.set_inter_op_parallelism_threads(1)

    cfg_id = config_id(params)
    rank_path = os.path.join(per_seed_dir, f"{cfg_id}__seed-{seed}.csv")

    model, val_mse = train_autoencoder(data, params, seed)
    importance = compute_gene_importance(model, genes, params["score_type"])
    save_full_ranking(importance, rank_path, params["top_k"])

    recovery_stats = evaluate_recovery(importance, KNOWN_DOWNSTREAM_GENES, params["top_k"])

    return {
        **params,
        "seed": seed,
        "val_mse": val_mse,
        **recovery_stats,
        "rank_path": rank_path,
        "config_id": cfg_id,
    }


def load_cluster_assignments(path: str) -> pd.DataFrame:
    df = pd.read_csv(path)
    lowercase_columns = {column.lower(): column for column in df.columns}

    gene_candidates = ["gene", "gene_id", "genes", "id", "attribution"]
    cluster_candidates = ["cluster", "cluster_id", "kmeans_cluster", "label"]

    gene_col = next((lowercase_columns[c] for c in gene_candidates if c in lowercase_columns), None)
    cluster_col = next((lowercase_columns[c] for c in cluster_candidates if c in lowercase_columns), None)

    if gene_col is None:
        raise ValueError(f"Could not identify a gene column in {path}. Columns: {list(df.columns)}")
    if cluster_col is None:
        raise ValueError(f"Could not identify a cluster column in {path}. Columns: {list(df.columns)}")

    assignments = df[[gene_col, cluster_col]].copy()
    assignments.columns = ["gene", "cluster"]
    return assignments


def load_expression_matrix(path: str, sep: str) -> pd.DataFrame:
    df = pd.read_csv(path, sep=sep)
    first_column = df.columns[0]
    return df.rename(columns={first_column: "gene"}).set_index("gene")


def run_cluster_pipeline(
    cluster_id: int,
    cluster_genes: list[str],
    expr_df: pd.DataFrame,
    outdir: str,
    pool_procs: int,
) -> dict:
    cluster_dir = os.path.join(outdir, f"cluster_{cluster_id:03d}")
    os.makedirs(cluster_dir, exist_ok=True)

    stage_a_file = os.path.join(cluster_dir, f"AE_stageA_results_cluster_{cluster_id}.csv")
    master_file = os.path.join(cluster_dir, f"AE_master_grid_results_cluster_{cluster_id}.csv")

    per_seed_dir = os.path.join(cluster_dir, "per_seed_ranks")
    top5_dir = os.path.join(cluster_dir, "top5_seeds")
    os.makedirs(per_seed_dir, exist_ok=True)
    os.makedirs(top5_dir, exist_ok=True)

    top5_summary_file = os.path.join(top5_dir, f"top5_seeds_cluster_{cluster_id}_summary.csv")

    genes_in_expr = expr_df.index.intersection(cluster_genes)
    if len(genes_in_expr) < 10:
        return {
            "cluster": cluster_id,
            "status": "SKIP",
            "reason": f"Too few genes after filtering: {len(genes_in_expr)}",
        }

    subset = expr_df.loc[genes_in_expr].copy()
    data = subset.T

    data = (data - data.mean(axis=0)) / data.std(axis=0, ddof=0)
    data = data.replace([np.inf, -np.inf], np.nan).fillna(0.0)

    if TOP_N_MOST_VARIABLE_GENES is not None:
        variances = data.var(axis=0, ddof=0)
        keep_genes = variances.nlargest(TOP_N_MOST_VARIABLE_GENES).index
        data = data[keep_genes]

    genes = data.columns.tolist()
    context = get_context("spawn")

    set_seed(GLOBAL_SEED)

    stage_a_rows = []
    for trial in range(MAX_TRIALS_STAGE_A):
        params = sample_config(PARAM_GRID)
        requested_depth = params["depth"]

        shallow_params = params.copy()
        shallow_params["depth"] = "shallow"

        with context.Pool(processes=pool_procs) as pool:
            jobs = [(seed, shallow_params, genes, data, per_seed_dir) for seed in SEEDS_STAGE_A]
            shallow_runs = pool.map(run_one_seed, jobs, chunksize=1)
        stage_a_rows.extend(shallow_runs)

        mean_kdg = float(np.mean([result["kdg_topk"] for result in shallow_runs]))

        if requested_depth == "deep" and mean_kdg >= KDG_THRESHOLD_FOR_DEEP:
            deep_params = params.copy()
            deep_params["depth"] = "deep"
            with context.Pool(processes=pool_procs) as pool:
                jobs = [(seed, deep_params, genes, data, per_seed_dir) for seed in SEEDS_STAGE_A]
                deep_runs = pool.map(run_one_seed, jobs, chunksize=1)
            stage_a_rows.extend(deep_runs)

        if (trial + 1) % 10 == 0:
            print(f"[cluster {cluster_id}] Stage A trials: {trial + 1}/{MAX_TRIALS_STAGE_A} (pool={pool_procs})")

    df_stage_a = pd.DataFrame(stage_a_rows)
    df_stage_a.to_csv(stage_a_file, index=False)
    print(f"[cluster {cluster_id}] Stage A saved: {stage_a_file}")

    group_columns = [
        "encoding_dim",
        "depth",
        "activation",
        "l1_reg",
        "l2_reg",
        "dropout_rate",
        "noise_sigma",
        "lr",
        "batch_size",
        "top_k",
        "score_type",
    ]

    aggregated = (
        df_stage_a.groupby(group_columns)[["kdg_topk"]]
        .mean()
        .sort_values(["kdg_topk"], ascending=False)
    )
    top_configs = aggregated.head(TOP_N_FOR_STAGE_B).reset_index().to_dict(orient="records")

    stage_b_rows = []
    for params_cfg in top_configs:
        with context.Pool(processes=pool_procs) as pool:
            jobs = [(seed, params_cfg, genes, data, per_seed_dir) for seed in SEEDS_STAGE_B]
            config_results = pool.map(run_one_seed, jobs, chunksize=1)
        stage_b_rows.extend(config_results)
        print(f"[cluster {cluster_id}] Stage B complete for one configuration ({len(SEEDS_STAGE_B)} seeds).")

    df_stage_b = pd.DataFrame(stage_b_rows)

    df_all = pd.concat([df_stage_a, df_stage_b], ignore_index=True)
    df_all.to_csv(master_file, index=False)
    print(f"[cluster {cluster_id}] Master results saved: {master_file}")

    mse_min = float(df_all["val_mse"].min())
    mse_std = float(df_all["val_mse"].std(ddof=0))
    mse_cutoff = mse_min + mse_std

    df_filtered = df_all[df_all["val_mse"] <= mse_cutoff].copy()
    if df_filtered.empty:
        df_filtered = df_all.copy()

    df_sorted = df_filtered.sort_values(
        ["kdg_topk", "kdg_fold_enrichment", "val_mse"],
        ascending=[False, False, True]
    ).reset_index(drop=True)

    df_top5 = df_sorted.head(5).copy()

    keep_columns = [
        "seed",
        "val_mse",
        "kdg_topk",
        "kdg_fold_enrichment",
        "kdg_p_value",
        "kdg_expected",
        "kdg_in_universe",
        "n_universe",
        "config_id",
        "rank_path",
        *group_columns,
    ]

    df_top5_summary = df_top5[keep_columns]
    df_top5_summary.to_csv(top5_summary_file, index=False)

    for _, row in df_top5_summary.iterrows():
        src = row.get("rank_path", "")
        if isinstance(src, str) and os.path.isfile(src):
            destination = os.path.join(top5_dir, os.path.basename(src))
            try:
                pd.read_csv(src).to_csv(destination, index=False)
            except Exception:
                pass

    best = df_top5_summary.iloc[0].to_dict() if len(df_top5_summary) else {}
    return {
        "cluster": cluster_id,
        "status": "OK",
        "n_genes": len(genes),
        "mse_cut": mse_cutoff,
        "best_seed": best.get("seed"),
        "best_kdg_topk": best.get("kdg_topk"),
        "best_kdg_fold_enrichment": best.get("kdg_fold_enrichment"),
        "best_kdg_p_value": best.get("kdg_p_value"),
        "kdg_in_universe_best": best.get("kdg_in_universe"),
        "cluster_dir": cluster_dir,
    }


def main() -> None:
    os.makedirs(BASE_OUTDIR, exist_ok=True)

    assignments = load_cluster_assignments(CLUSTER_ASSIGNMENT_FILE)
    assignments["cluster"] = pd.to_numeric(assignments["cluster"], errors="coerce")
    assignments = assignments.dropna(subset=["cluster"])
    assignments["cluster"] = assignments["cluster"].astype(int)

    expr = load_expression_matrix(EXPR_MATRIX_FILE, EXPR_SEP)

    max_parallel = max(1, int(MAX_CLUSTERS_IN_PARALLEL))
    total_cpus = max(1, int(TOTAL_CPUS))
    cpus_per_cluster = max(1, total_cpus // max_parallel)

    print(
        f"TOTAL_CPUS={total_cpus} | "
        f"MAX_CLUSTERS_IN_PARALLEL={max_parallel} | "
        f"CPUS_PER_CLUSTER={cpus_per_cluster}"
    )
    print(f"Clusters requested: {CLUSTERS_OF_INTEREST}")
    print(f"Expression genes: {expr.shape[0]} | Samples: {expr.shape[1]}")

    jobs = []
    for cluster_id in CLUSTERS_OF_INTEREST:
        cluster_genes = (
            assignments.loc[assignments["cluster"] == int(cluster_id), "gene"]
            .astype(str)
            .unique()
            .tolist()
        )
        jobs.append((int(cluster_id), cluster_genes))

    results = []
    with ProcessPoolExecutor(max_workers=max_parallel) as executor:
        futures = [
            executor.submit(run_cluster_pipeline, cluster_id, cluster_genes, expr, BASE_OUTDIR, cpus_per_cluster)
            for cluster_id, cluster_genes in jobs
        ]

        for future in as_completed(futures):
            try:
                result = future.result()
                results.append(result)

                best_fold = result.get("best_kdg_fold_enrichment")
                best_fold_str = f"{best_fold:.3f}" if best_fold is not None else "NA"

                best_p = result.get("best_kdg_p_value")
                best_p_str = f"{best_p:.2e}" if best_p is not None else "NA"

                print(
                    f"[DONE] cluster={result.get('cluster')} "
                    f"status={result.get('status')} "
                    f"best_kdg_topk={result.get('best_kdg_topk')} "
                    f"best_kdg_fold={best_fold_str} "
                    f"best_kdg_p={best_p_str} "
                    f"dir={result.get('cluster_dir')}"
                )
            except Exception as exc:
                print(f"[ERROR] Cluster job failed: {exc}")

    summary_path = os.path.join(BASE_OUTDIR, "multi_cluster_summary.csv")
    pd.DataFrame(results).to_csv(summary_path, index=False)
    print(f"Multi-cluster summary saved: {summary_path}")


